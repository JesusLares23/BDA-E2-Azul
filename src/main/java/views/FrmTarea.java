    private void cargarDatos() {
        int fila = tblTarea.getSelectedRow();
        if (fila >= 0) {
            txtId.setText(tblTarea.getValueAt(fila, 0).toString());
            txtIdVoluntario.setText(tblTarea.getValueAt(fila, 3).toString());
            txtIdAnimal.setText(tblTarea.getValueAt(fila, 4).toString());
            txtIdRefugio.setText(tblTarea.getValueAt(fila, 5).toString());
            jTextAreaActividad.setText(tblTarea.getValueAt(fila, 2).toString());

            String fechaStr = tblTarea.getValueAt(fila, 1).toString();
            try {
                LocalDateTime fecha = LocalDateTime.parse(fechaStr);
                spinnerDia.setValue(fecha.getDayOfMonth());
                spinnerMes.setValue(fecha.getMonthValue());
                txtAnio.setText(String.valueOf(fecha.getYear()));
            } catch (Exception e) {
                // si el formato no parsea directamente se deja como está
            }
        }
    }
    
