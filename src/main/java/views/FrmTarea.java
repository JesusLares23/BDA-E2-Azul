    //Validacion  del campo de texto ID Voluntario
    public boolean validarTxtIdVoluntario() {
        String val = txtIdVoluntario.getText().trim();
        if (val.isEmpty()) {
            lblVoluntarioObligatorio.setText("Campo obligatorio");
            lblVoluntarioObligatorio.setVisible(true);
            txtIdVoluntario.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
            return false;
        }
        if (!val.matches("\\d+")) {
            lblVoluntarioObligatorio.setText("Solo números");
            lblVoluntarioObligatorio.setVisible(true);
            txtIdVoluntario.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
            return false;
        }
        if (voluntarioDAO.obtenerPorId(Integer.parseInt(val)) == null) {
            lblVoluntarioObligatorio.setText("ID no existe");
            lblVoluntarioObligatorio.setVisible(true);
            txtIdVoluntario.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
            return false;
        }
        txtIdVoluntario.setBorder(BorderFactory.createLineBorder(Color.BLACK, 1));
        lblVoluntarioObligatorio.setVisible(false);
        return true;
    }
    
