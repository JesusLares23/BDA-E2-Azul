    //Validacion  de la fecha
    public boolean validarFecha() {
        String anioStr = txtAnio.getText().trim();
        if (anioStr.isEmpty()) {
            lblAnioObligatorio.setText("Campo obligatorio");
            lblAnioObligatorio.setVisible(true);
            txtAnio.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
            return false;
        }
        if (!anioStr.matches("\\d{4}")) {
            lblAnioObligatorio.setText("Año no válido");
            lblAnioObligatorio.setVisible(true);
            txtAnio.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
            return false;
        }
        try {
            int anio = Integer.parseInt(anioStr);
            int mes = (Integer) spinnerMes.getValue();
            int dia = (Integer) spinnerDia.getValue();
            LocalDate fecha = LocalDate.of(anio, mes, dia);
            if (fecha.isAfter(LocalDate.now())) {
                lblAnioObligatorio.setText("Fecha futura no válida");
                lblAnioObligatorio.setVisible(true);
                txtAnio.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
                return false;
            }
        } catch (Exception e) {
            lblAnioObligatorio.setText("Fecha no válida");
            lblAnioObligatorio.setVisible(true);
            txtAnio.setBorder(BorderFactory.createLineBorder(Color.RED, 1));
            return false;
        }
        txtAnio.setBorder(BorderFactory.createLineBorder(Color.BLACK, 1));
        lblAnioObligatorio.setVisible(false);
        return true;
    }
    
  
    
    //  validador del formulario
    public boolean validarFormulario() {
        boolean v   = validarTxtIdVoluntario();
        boolean a   = validarTxtIdAnimal();
        boolean r   = validarTxtIdRefugio();
        boolean act = validarActividad();
        boolean f   = validarFecha();
        return v && a && r && act && f;
    }

    // Junta los datos de la fecha
    private LocalDateTime construirFecha() {
        int anio = Integer.parseInt(txtAnio.getText().trim());
        int mes  = (Integer) spinnerMes.getValue();
        int dia  = (Integer) spinnerDia.getValue();
        return LocalDateTime.of(anio, mes, dia, 0, 0);
    }
    
    // Registra la tarea sin antes de aplicar las validaciones a los datos
    private void registrarTarea() {
        try {
            if (!validarFormulario()) {
                JOptionPane.showMessageDialog(this,
                        "No se pudo registrar. Verifique los campos.",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            boolean exito = tareaController.agregarTarea(
                    construirFecha(),
                    jTextAreaActividad.getText().trim(),
                    Integer.parseInt(txtIdVoluntario.getText().trim()),
                    Integer.parseInt(txtIdAnimal.getText().trim()),
                    Integer.parseInt(txtIdRefugio.getText().trim())
            );
            if (exito) {
                JOptionPane.showMessageDialog(this, "Tarea registrada correctamente.");
            } else {
                JOptionPane.showMessageDialog(this, "No se pudo guardar la tarea.");
            }
            cargarTareas();
            limpiarCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
        private void actualizar() {
        try {
            if (!validarFormulario()) {
                JOptionPane.showMessageDialog(this,
                        "No se pudo actualizar. Verifique los campos.",
                        "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            int id = Integer.parseInt(txtId.getText().trim());
            int confirm = JOptionPane.showConfirmDialog(this,
                    "¿Seguro que quieres actualizar esta tarea?",
                    "Confirmar actualización", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                boolean exito = tareaController.actualizarTarea(
                        id,
                        construirFecha(),
                        jTextAreaActividad.getText().trim(),
                        Integer.parseInt(txtIdVoluntario.getText().trim()),
                        Integer.parseInt(txtIdAnimal.getText().trim()),
                        Integer.parseInt(txtIdRefugio.getText().trim())
                );
                if (exito) {
                    JOptionPane.showMessageDialog(this, "Tarea actualizada correctamente.");
                    cargarTareas();
                    limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Ocurrió un error al actualizar la tarea.",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
        
            private void eliminar() {
        try {
            int id = Integer.parseInt(txtId.getText().trim());
            int confirm = JOptionPane.showConfirmDialog(this,
                    "¿Seguro que quieres eliminar esta tarea?",
                    "Confirmar eliminación", JOptionPane.YES_NO_OPTION);
            if (confirm == JOptionPane.YES_OPTION) {
                boolean exito = tareaController.eliminarTarea(id);
                if (exito) {
                    JOptionPane.showMessageDialog(this, "Tarea eliminada correctamente.");
                    cargarTareas();
                    limpiarCampos();
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Ocurrió un error al eliminar la tarea.",
                            "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,
                    "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
       
    
    
private void filtrarTabla(String texto) {
    if (texto.trim().isEmpty()) {
        tblTarea.setModel(tareaController.obtenerTablaTareas());
    } else {
        tblTarea.setModel(tareaController.buscarTablaPorActividad(texto));
    }
}
    
    
    
